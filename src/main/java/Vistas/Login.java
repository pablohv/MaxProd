    private void ingresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarActionPerformed

        if (!usuario.getText().trim().equals("") && !pass.getText().trim().equals("")) {

            if (soyTecnico.isSelected()) {
                Usuario user = new Usuario(usuario.getText().trim(), pass.getText().trim());
                UsuarioDaoImpl userDao = new UsuarioDaoImpl();
                boolean existe = userDao.ingresarTecnico(user);

                if (existe) {
                    //JOptionPane.showMessageDialog(this, "Datos correctos");
                    Tecnico alta = new Tecnico();
                    alta.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Datos incorrectos");
                }
            } else {

                Usuario user = new Usuario(usuario.getText().trim(), pass.getText().trim());
                UsuarioDaoImpl userDao = new UsuarioDaoImpl();
                boolean existe = userDao.ingresar(user);

                if (existe) {
                    //JOptionPane.showMessageDialog(this, "Datos correctos");
                    Administrador alta = new Administrador();
                    alta.setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Datos incorrectos");
                }
            }

        } else {
            JOptionPane.showMessageDialog(this, "Te falta algun valor");
        }

    }//GEN-LAST:event_ingresarActionPerformed

